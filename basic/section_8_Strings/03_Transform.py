# str(x) string
a = str(123)
b = str([1, 2, 3])
c = str(True)
d = str(False)
print(a)
print(b)
print(b[0])
print(c)
print(d)
print(c+d)

# ascii(x) ASCII
a = ascii('Hello')
print(a)

# bin(x) 2
a = bin(1234)
print(a)

# oct(x) 8
a = oct(1234)
print(a)

# hex(x) 16
a = hex(1234)
print(a)

# chr(x) Unicode
a = chr(101)
b = chr(202)
c = chr(9999)
print(a)
print(b)
print(c)

# ord(x) Reverse Unicode
a = ord('e')
b = ord('E')
c = ord('s')
print(a)
print(b)
print(c)